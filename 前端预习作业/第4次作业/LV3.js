var a = { n: 1 }//声明变量a
var b = a//声明变量b并将a的值赋予b
a.x = a = { n: 2 }//.的运算优先级高于=
// 1.先运行第二个 = 的计算：即把a的值赋为{ n: 2 }
// 2.再运行第二个 = 的计算：由于a已被引用，故在b中创建了一个类似与n的x属性，并将x属性的值赋为a即{ n: 2 }；
console.log(a)//输出{ n: 2 }
console.log(b)//输出{ n: 1, x: { n: 2 } }
a.n = 3//将a中属性n的值赋为3；由于第三条语句中将a的引用值赋给了属性x，故x的值也发生改变
console.log(b)//输出{ n: 1, x: { n: 3 } }